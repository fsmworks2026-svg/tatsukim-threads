/**
 * アフィリ投稿テキストを生成する
 * @param {{ name, price, catchcopy, affiliateUrl }} item Amazon商品情報
 * @param {{ label, hashtags }} category カテゴリ情報
 * @param {number} patternIndex 0〜3のパターン番号
 * @returns {string} 投稿テキスト
 */
export function generateAffiliatePost(item, category, patternIndex) {
  const pain = PAIN_COPY[category.label] ?? 'ちょっといいものを取り入れたくて。';
  const { name, price, catchcopy, affiliateUrl } = item;
  const tags = `${category.hashtags} #Amazon #PR`;

  const patterns = [
    // パターン0: 悩み→発見型
    `${pain}

Amazonで見つけた「${name}」が気になってる。

${catchcopy ? `「${catchcopy}」\n\n` : ''}${price}円〜（Amazon）
${affiliateUrl}

${tags}`,

    // パターン1: 使用シーン→共感型
    `${pain}

いろいろ調べてたら
Amazonで「${name}」に出会った。

${catchcopy ? `${catchcopy}\n\n` : ''}これ、ちょっと試してみようかな。

${price}円〜（Amazon）
${affiliateUrl}

${tags}`,

    // パターン2: 価格×驚き型
    `${pain}

${price}円でこのクオリティ、って正直驚いた。

「${name}」、Amazonで見つけた。

${catchcopy ? `「${catchcopy}」\n\n` : ''}気になる人は見てみて。

${affiliateUrl}

${tags}`,

    // パターン3: 背中押し型
    `ずっと気になってたやつ、ついに調べた。

「${name}」

${catchcopy ? `${catchcopy}\n\n` : ''}${pain}

${price}円〜（Amazon）
${affiliateUrl}

${tags}`,
  ];

  return patterns[patternIndex % patterns.length];
}

